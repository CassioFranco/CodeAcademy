<?php

    // +=
    $a = 0;
    $a += 10; // $a - $a + 10

    echo $a . "\n";

    // -=
    $b = 0;
    $b -=5; // $b = $b - 5

    echo $b . "\n";

    // *=
    $c = 5;
    $c -=2; // $c - $c * 10

    echo $c . "\n";

    // /=
    $d = 5;
    $d /=2; // $d = $d / 2

    echo $d . "\n";

    // %=
    $e = 5;
    $e %=2; // $e = $e % 2

    echo $e . "\n";


    // utilizando com variaveis
    $f = 10;
    $x = 20;

    $f += $x; //$f = $f + $x

    echo $f . "\n";

